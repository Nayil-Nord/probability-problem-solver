"""
Определяет операцию для расчёта вероятности событий.
"""

while True:
   print("\nОпределитель операции для расчёта вероятности\n")

   if input("События могут произойти одновременно? (да/нет)\nПример: выпадение орла И четного числа на кубике\nВаш ответ: ").lower().strip() == "да":
       print("\nИспользуйте умножение вероятностей: P(A и B) = P(A) × P(B)")
       print("Пример: P(орел и четное) = P(орел) × P(четное) = 1/2 × 1/2 = 1/4")
   
   else: 
       print("\nИспользуйте сложение вероятностей: P(A или B) = P(A) + P(B)")
       print("Пример: P(орел или решка) = P(орел) + P(решка) = 1/2 + 1/2 = 1")
      
   if input("\nХотите попробовать еще раз? (да/нет): ").lower().strip() != "да":
       print("\nСпасибо за использование программы!")
       break